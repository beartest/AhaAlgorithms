//第2节 炸弹人
function bomber() {
	var map = [
		['#', '#', '#', '#', '#', '#', '#', '#', '#', '#', '#', '#', '#'],
		['#', 'G', 'G', '.', 'G', 'G', 'G', '#', 'G', 'G', 'G', '.', '#'],
		['#', '#', '#', '.', '#', 'G', '#', 'G', '#', 'G', '#', 'G', '#'],
		['#', '.', '.', '.', '.', '.', '.', '.', '#', '.', '.', 'G', '#'],
		['#', 'G', '#', '.', '#', '#', '#', '.', '#', 'G', '#', 'G', '#'],
		['#', 'G', 'G', '.', 'G', 'G', 'G', '.', '#', '.', 'G', 'G', '#'],
		['#', 'G', '#', '.', '#', 'G', '#', '.', '#', '.', '#', '#', '#'],
		['#', '#', 'G', '.', '.', '.', 'G', '.', '.', '.', '.', '.', '#'],
		['#', 'G', '#', '.', '#', 'G', '#', '#', '#', '.', '#', 'G', '#'],
		['#', '.', '.', '.', 'G', '#', 'G', 'G', 'G', '.', 'G', 'G', '#'],
		['#', 'G', '#', '.', '#', 'G', '#', 'G', '#', '.', '#', 'G', '#'],
		['#', 'G', 'G', '.', 'G', 'G', 'G', '#', 'G', '.', 'G', 'G', '#'],
		['#', '#', '#', '#', '#', '#', '#', '#', '#', '#', '#', '#', '#'],
	];

	var row = map.length;
	var col = map[0].length;
	var max = 0,
		p, q;
	for(var i = 0; i < row; i++) {
		for(var j = 0; j < col; j++) {
			if(map[i][j] == '.') {
				var x = i;
				var y = j;
				var sum = 0;
				while(map[x][y] != '#') {
					if(map[x][y] == 'G') {
						sum++;
					}
					x--;
				}

				x = i;
				while(map[x][y] != '#') {
					if(map[x][y] == 'G') {
						sum++;
					}
					x++;
				}

				x = i;
				while(map[x][y] != '#') {
					if(map[x][y] == 'G') {
						sum++;
					}
					y--;
				}

				y = j;
				while(map[x][y] != '#') {
					if(map[x][y] == 'G') {
						sum++;
					}
					y++;
				}

				if(sum > max) {
					max = sum;
					p = i;
					q = j;
				}
			}

		}
	}

	console.log("在(" + p + "," + q + ")处，一共消灭" + max + "个敌人");
}

bomber();